def fetch_repositories_for_user(
    settings: Any,
    *,
    user_id: str,
    team_id: str,
    timeout_seconds: float = 2.5,
) -> list[str]:
    cache_key = cache_key_for_user(user_id=user_id, team_id=team_id)
    now = time.time()
    cached = GITHUB_REPO_OPTIONS_CACHE.get(cache_key)
    if cached and (now - cached[0]) < GITHUB_OPTION_CACHE_TTL_SECONDS:
        return list(cached[1])

    if settings.repo_indexer_enabled():
        indexer_payload = fetch_indexer_catalog_payload(
            settings,
            user_id=user_id,
            team_id=team_id,
            query="",
            top_k_repos=100,
            top_k_branches_per_repo=1,
            timeout_seconds=timeout_seconds,
        )
        indexer_repos = indexer_repo_slugs(indexer_payload)
        if indexer_repos:
            GITHUB_REPO_OPTIONS_CACHE[cache_key] = (now, indexer_repos)
            return list(indexer_repos)

    if not settings.github_user_oauth_enabled():
        return []
    token = resolve_github_user_token(user_id=user_id, team_id=team_id)
    if not token:
        return []

    repos: list[str] = []
    try:
        response = httpx.get(
            f"{settings.github_api_base.rstrip('/')}/user/repos",
            params={
                "per_page": 100,
                "sort": "updated",
                "affiliation": "owner,collaborator,organization_member",
            },
            headers=github_api_headers(token=token),
            timeout=timeout_seconds,
        )
        if response.status_code == 401:
            raise GitHubAuthError("GitHub OAuth token expired while loading repos.")
        response.raise_for_status()
        payload = response.json()
        if isinstance(payload, list):
            for item in payload:
                if not isinstance(item, dict):
                    continue
                full_name = str(item.get("full_name") or "").strip()
                owner, repo = parse_repo_slug(full_name)
                if owner and repo:
                    repos.append(f"{owner}/{repo}")
    except GitHubAuthError:
        raise
    except Exception as e:
        logger.warning(
            "slack_github_repo_fetch_failed user=%s team=%s url=%s error=%s",
            user_id,
            team_id,
            f"{settings.github_api_base.rstrip('/')}/user/repos",
            e,
            exc_info=True,
        )
        repos = []

    deduped = dedupe(repos)
    if deduped:
        GITHUB_REPO_OPTIONS_CACHE[cache_key] = (now, deduped)
    else:
        GITHUB_REPO_OPTIONS_CACHE.pop(cache_key, None)
    return deduped


def fetch_branches_for_repo(
    settings: Any,
    *,
    user_id: str,
    team_id: str,
    repo_slug: str,
    timeout_seconds: float = 2.5,
) -> list[str]:
    owner, repo = parse_repo_slug(repo_slug)
    if not owner or not repo:
        return []

    cache_key = f"{cache_key_for_user(user_id=user_id, team_id=team_id)}:{owner}/{repo}"
    now = time.time()
    cached = GITHUB_BRANCH_OPTIONS_CACHE.get(cache_key)
    if cached and (now - cached[0]) < GITHUB_OPTION_CACHE_TTL_SECONDS:
        return list(cached[1])

    if settings.repo_indexer_enabled():
        indexer_payload = fetch_indexer_catalog_payload(
            settings,
            user_id=user_id,
            team_id=team_id,
            query=f"{owner}/{repo}",
            top_k_repos=20,
            top_k_branches_per_repo=max(int(getattr(settings, "indexer_top_k_branches_per_repo", 8) or 8), 1),
            timeout_seconds=timeout_seconds,
        )
        suggestion = indexer_repo_suggestion_for_slug(indexer_payload, owner=owner, repo=repo)
        if suggestion:
            branches, default_branch = indexer_branch_names_and_default(settings, suggestion=suggestion)
            if branches:
                GITHUB_BRANCH_OPTIONS_CACHE[cache_key] = (now, list(branches))
                if default_branch:
                    GITHUB_REPO_DEFAULT_BRANCH_CACHE[cache_key] = (now, default_branch)
                return list(branches)

    if not settings.github_user_oauth_enabled():
        return []
    token = resolve_github_user_token(user_id=user_id, team_id=team_id)
    if not token:
        return []

    default_from_git, worktree_branches = fetch_branches_via_worktree_catalog(
        settings,
        owner=owner,
        repo=repo,
        token=token,
        timeout_seconds=max(timeout_seconds, BRANCH_WORKTREE_FETCH_TIMEOUT_SECONDS),
    )
    if worktree_branches:
        GITHUB_BRANCH_OPTIONS_CACHE[cache_key] = (now, list(worktree_branches))
        if default_from_git:
            GITHUB_REPO_DEFAULT_BRANCH_CACHE[cache_key] = (now, default_from_git)
        return list(worktree_branches)

    branches: list[str] = []
    try:
        response = httpx.get(
            f"{settings.github_api_base.rstrip('/')}/repos/{owner}/{repo}/branches",
            params={"per_page": 100},
            headers=github_api_headers(token=token),
            timeout=timeout_seconds,
        )
        if response.status_code == 401:
            raise GitHubAuthError("GitHub OAuth token expired while loading branches.")
        response.raise_for_status()
        payload = response.json()
        if isinstance(payload, list):
            for item in payload:
                if not isinstance(item, dict):
                    continue
                name = str(item.get("name") or "").strip()
                if name:
                    branches.append(name)
    except GitHubAuthError:
        raise
    except Exception as e:
        logger.warning(
            "slack_github_branch_fetch_failed user=%s team=%s repo=%s/%s error=%s",
            user_id,
            team_id,
            owner,
            repo,
            e,
            exc_info=True,
        )
        branches = []

    deduped = dedupe(branches)
    default_cached = str((GITHUB_REPO_DEFAULT_BRANCH_CACHE.get(cache_key) or (0.0, ""))[1] or "").strip()
    sorted_branches = sort_branch_names_for_selection(
        settings,
        branches=deduped,
        default_branch=default_cached,
        worktree_branches=[],
    )
    if sorted_branches:
        GITHUB_BRANCH_OPTIONS_CACHE[cache_key] = (now, sorted_branches)
    else:
        GITHUB_BRANCH_OPTIONS_CACHE.pop(cache_key, None)
    return sorted_branches


def fetch_default_branch_for_repo(
    settings: Any,
    *,
    user_id: str,
    team_id: str,
    repo_slug: str,
    timeout_seconds: float = 2.5,
) -> str:
    owner, repo = parse_repo_slug(repo_slug)
    if not owner or not repo:
        return ""

    cache_key = f"{cache_key_for_user(user_id=user_id, team_id=team_id)}:{owner}/{repo}"
    now = time.time()
    cached = GITHUB_REPO_DEFAULT_BRANCH_CACHE.get(cache_key)
    if cached and (now - cached[0]) < GITHUB_OPTION_CACHE_TTL_SECONDS:
        return str(cached[1] or "")

    if settings.repo_indexer_enabled():
        indexer_payload = fetch_indexer_catalog_payload(
            settings,
            user_id=user_id,
            team_id=team_id,
            query=f"{owner}/{repo}",
            top_k_repos=20,
            top_k_branches_per_repo=max(int(getattr(settings, "indexer_top_k_branches_per_repo", 8) or 8), 1),
            timeout_seconds=timeout_seconds,
        )
        suggestion = indexer_repo_suggestion_for_slug(indexer_payload, owner=owner, repo=repo)
        if suggestion:
            _branches, default_from_indexer = indexer_branch_names_and_default(settings, suggestion=suggestion)
            if default_from_indexer:
                GITHUB_REPO_DEFAULT_BRANCH_CACHE[cache_key] = (now, default_from_indexer)
                return default_from_indexer

    if not settings.github_user_oauth_enabled():
        return ""
    token = resolve_github_user_token(user_id=user_id, team_id=team_id)
    if not token:
        return ""

    default_from_git, worktree_branches = fetch_branches_via_worktree_catalog(
        settings,
        owner=owner,
        repo=repo,
        token=token,
        timeout_seconds=max(timeout_seconds, BRANCH_WORKTREE_FETCH_TIMEOUT_SECONDS),
    )
    if default_from_git:
        GITHUB_REPO_DEFAULT_BRANCH_CACHE[cache_key] = (now, default_from_git)
        if worktree_branches:
            GITHUB_BRANCH_OPTIONS_CACHE[cache_key] = (now, list(worktree_branches))
        return default_from_git

    default_branch = ""
    try:
        response = httpx.get(
            f"{settings.github_api_base.rstrip('/')}/repos/{owner}/{repo}",
            headers=github_api_headers(token=token),
            timeout=timeout_seconds,
        )
        if response.status_code == 401:
            raise GitHubAuthError("GitHub OAuth token expired while loading default branch.")
        response.raise_for_status()
        payload = response.json()
        if isinstance(payload, dict):
            default_branch = str(payload.get("default_branch") or "").strip()
    except GitHubAuthError:
        raise
    except Exception as e:
        logger.warning(
            "slack_github_default_branch_fetch_failed user=%s team=%s repo=%s/%s error=%s",
            user_id,
            team_id,
            owner,
            repo,
            e,
            exc_info=True,
        )
        default_branch = ""

    if default_branch:
        GITHUB_REPO_DEFAULT_BRANCH_CACHE[cache_key] = (now, default_branch)
    else:
        GITHUB_REPO_DEFAULT_BRANCH_CACHE.pop(cache_key, None)
    return default_branch
