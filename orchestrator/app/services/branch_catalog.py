def dedupe(values: list[str]) -> list[str]:
    seen: set[str] = set()
    output: list[str] = []
    for raw in values:
        item = str(raw).strip()
        if not item:
            continue
        key = item.lower()
        if key in seen:
            continue
        seen.add(key)
        output.append(item)
    return output


def cache_key_for_user(*, user_id: str, team_id: str) -> str:
    return f"{(team_id or '').strip()}:{(user_id or '').strip()}"


