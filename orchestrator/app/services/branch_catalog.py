def fetch_branches_via_worktree_catalog(
    settings: Any,
    *,
    owner: str,
    repo: str,
    token: str,
    timeout_seconds: float,
) -> tuple[str, list[str]]:
    if shutil.which("git") is None:
        return "", []

    repo_path, selection_worktree = branch_catalog_paths(owner=owner, repo=repo)
    repo_path.parent.mkdir(parents=True, exist_ok=True)
    clone_url = f"https://github.com/{owner}/{repo}.git"
    auth_header = github_basic_auth_extraheader(token)
    timeout = max(int(timeout_seconds), 2)

    try:
        if not (repo_path / ".git").exists():
            if repo_path.exists():
                shutil.rmtree(repo_path, ignore_errors=True)
            run_git_command(
                cmd=[
                    "git",
                    "-c",
                    f"http.https://github.com/.extraheader={auth_header}",
                    "clone",
                    "--filter=blob:none",
                    "--depth",
                    "1",
                    "--no-single-branch",
                    "--no-checkout",
                    clone_url,
                    str(repo_path),
                ],
                cwd=repo_path.parent,
                timeout_seconds=timeout,
            )
        else:
            run_git_command(
                cmd=[
                    "git",
                    "-c",
                    f"http.https://github.com/.extraheader={auth_header}",
                    "fetch",
                    "--prune",
                    "--no-tags",
                    "--depth",
                    "1",
                    "origin",
                    "+refs/heads/*:refs/remotes/origin/*",
                ],
                cwd=repo_path,
                timeout_seconds=timeout,
            )
        try:
            run_git_command(
                cmd=[
                    "git",
                    "-c",
                    f"http.https://github.com/.extraheader={auth_header}",
                    "remote",
                    "set-head",
                    "origin",
                    "-a",
                ],
                cwd=repo_path,
                timeout_seconds=timeout,
            )
        except Exception:
            logger.exception("slack_github_branch_catalog_set_head_failed")
    except Exception as e:
        logger.warning(
            "slack_github_branch_catalog_sync_failed owner=%s repo=%s error=%s",
            owner,
            repo,
            e,
            exc_info=True,
        )
        return "", []

    default_branch = remote_default_branch_from_git(repo_path=repo_path)
    if default_branch:
        try:
            run_git_command(cmd=["git", "worktree", "prune"], cwd=repo_path, timeout_seconds=10)
        except Exception:
            logger.exception("slack_github_branch_catalog_worktree_prune_failed")
        # Keep one detached selection worktree per repo so branch selection can use git-worktree state.
        if not selection_worktree.exists():
            try:
                run_git_command(
                    cmd=[
                        "git",
                        "worktree",
                        "add",
                        "--detach",
                        str(selection_worktree),
                        f"origin/{default_branch}",
                    ],
                    cwd=repo_path,
                    timeout_seconds=20,
                )
            except Exception:
                logger.exception("slack_github_branch_catalog_worktree_seed_failed")

    raw_branches = list_remote_branches_sorted_by_recent_commit(repo_path=repo_path)
    worktree_branches = list_worktree_branches(repo_path=repo_path)
    sorted_branches = sort_branch_names_for_selection(
        settings,
        branches=raw_branches,
        default_branch=default_branch,
        worktree_branches=worktree_branches,
    )
    return default_branch, sorted_branches


