def github_api_headers(*, token: str) -> dict[str, str]:
    return {
        "Authorization": f"Bearer {token}",
        "Accept": "application/vnd.github+json",
        "X-GitHub-Api-Version": "2022-11-28",
    }


def github_basic_auth_extraheader(token: str) -> str:
    raw = f"x-access-token:{token}".encode("utf-8")
    encoded = base64.b64encode(raw).decode("ascii")
    return f"AUTHORIZATION: basic {encoded}"


def run_git_command(*, cmd: list[str], cwd: Path, timeout_seconds: int) -> str:
    result = subprocess.run(
        cmd,
        cwd=str(cwd),
        capture_output=True,
        text=True,
        timeout=max(timeout_seconds, 1),
    )
    if result.returncode != 0:
        message = result.stderr.strip() or result.stdout.strip() or "git command failed"
        raise RuntimeError(message)
    return str(result.stdout or "").strip()


def branch_catalog_paths(*, owner: str, repo: str) -> tuple[Path, Path]:
    normalized = f"{owner.strip().lower()}/{repo.strip().lower()}"
    digest = hashlib.sha1(normalized.encode("utf-8")).hexdigest()[:12]
    owner_slug = re.sub(r"[^a-z0-9_.-]+", "-", owner.strip().lower()).strip("-") or "owner"
    repo_slug = re.sub(r"[^a-z0-9_.-]+", "-", repo.strip().lower()).strip("-") or "repo"
    root = BRANCH_WORKTREE_CATALOG_ROOT / f"{owner_slug}--{repo_slug}--{digest}"
    return root / "repo", root / BRANCH_WORKTREE_PATH_NAME


def remote_default_branch_from_git(*, repo_path: Path) -> str:
    try:
        raw = run_git_command(
            cmd=["git", "symbolic-ref", "refs/remotes/origin/HEAD"],
            cwd=repo_path,
            timeout_seconds=10,
        )
    except Exception:
        return ""
    marker = "refs/remotes/origin/"
    if raw.startswith(marker):
        return raw[len(marker) :].strip()
    return ""


def list_worktree_branches(*, repo_path: Path) -> list[str]:
    try:
        raw = run_git_command(
            cmd=["git", "worktree", "list", "--porcelain"],
            cwd=repo_path,
            timeout_seconds=10,
        )
    except Exception:
        return []

    branches: list[str] = []
    seen: set[str] = set()
    marker = "refs/heads/"
    for line in raw.splitlines():
        text = str(line or "").strip()
        if not text.startswith("branch "):
            continue
        value = text[len("branch ") :].strip()
        if value.startswith(marker):
            value = value[len(marker) :].strip()
        if not value:
            continue
        key = value.lower()
        if key in seen:
            continue
        seen.add(key)
        branches.append(value)
    return branches


def list_remote_branches_sorted_by_recent_commit(*, repo_path: Path) -> list[str]:
    try:
        raw = run_git_command(
            cmd=[
                "git",
                "for-each-ref",
                "refs/remotes/origin",
                "--sort=-committerdate",
                "--format=%(refname:short)",
            ],
            cwd=repo_path,
            timeout_seconds=20,
        )
    except Exception:
        return []

    branches: list[str] = []
    seen: set[str] = set()
    for line in raw.splitlines():
        value = str(line or "").strip()
        if not value.startswith("origin/"):
            continue
        name = value[len("origin/") :].strip()
        if not name or name == "HEAD":
            continue
        key = name.lower()
        if key in seen:
            continue
        seen.add(key)
        branches.append(name)
    return branches


# ---------------------------------------------------------------------------
# Branch sorting
# ---------------------------------------------------------------------------

def sort_branch_names_for_selection(
    settings: Any,
    *,
    branches: list[str],
    default_branch: str = "",
    worktree_branches: list[str] | None = None,
) -> list[str]:
    normalized_default = str(default_branch or "").strip().lower()
    worktree_set = {str(x or "").strip().lower() for x in (worktree_branches or []) if str(x or "").strip()}
    candidate_rank = {name: idx for idx, name in enumerate(STABLE_BASE_BRANCH_CANDIDATES)}

    cleaned: list[str] = []
    seen: set[str] = set()
    for item in branches:
        branch = str(item or "").strip()
        if not branch:
            continue
        key = branch.lower()
        if key in seen:
            continue
        seen.add(key)
        cleaned.append(branch)

    indexed = list(enumerate(cleaned))

    def _bucket(branch_name: str) -> tuple[int, int]:
        lowered = branch_name.lower()
        if lowered and lowered == normalized_default:
            return (0, 0)
        is_worktree = lowered in worktree_set
        is_generated = is_autogenerated_branch(settings, branch_name)
        stable_idx = candidate_rank.get(lowered)
        if is_worktree and not is_generated:
            return (1, 0)
        if stable_idx is not None and not is_generated:
            return (2, stable_idx)
        if not is_generated:
            return (3, 0)
        if is_worktree:
            return (4, 0)
        return (5, 0)

    indexed.sort(key=lambda pair: (*_bucket(pair[1]), pair[0], pair[1].lower()))
    return [item[1] for item in indexed]


