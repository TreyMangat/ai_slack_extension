def autogenerated_branch_prefixes(settings: Any) -> tuple[str, ...]:
    prefixes: list[str] = ["prfactory/"]
    configured = str(getattr(settings, "llm_push_branch_prefix", "") or "").strip().strip("/").lower()
    if configured:
        prefixes.append(f"{configured}/")
    unique: list[str] = []
    for item in prefixes:
        value = str(item or "").strip().lower()
        if not value:
            continue
        if value not in unique:
            unique.append(value)
    return tuple(unique)


def is_autogenerated_branch(settings: Any, branch_name: str) -> bool:
    normalized = str(branch_name or "").strip().lower()
    if not normalized:
        return False
    return any(normalized.startswith(prefix) for prefix in autogenerated_branch_prefixes(settings))


def stable_branch_fallback(
    settings: Any,
    *,
    branches: list[str],
    default_branch: str = "",
) -> str:
    normalized_default = str(default_branch or "").strip().lower()
    cleaned: list[str] = []
    seen: set[str] = set()
    for item in branches:
        branch = str(item or "").strip()
        if not branch:
            continue
        key = branch.lower()
        if key in seen:
            continue
        seen.add(key)
        cleaned.append(branch)
    if normalized_default and not is_autogenerated_branch(settings, normalized_default):
        return default_branch.strip()
    lower_to_actual = {item.lower(): item for item in cleaned}
    for candidate in STABLE_BASE_BRANCH_CANDIDATES:
        match = lower_to_actual.get(candidate)
        if match and not is_autogenerated_branch(settings, match):
            return match
    for branch in cleaned:
        lowered = branch.lower()
        if lowered == normalized_default:
            continue
        if not is_autogenerated_branch(settings, branch):
            return branch
    return ""


def normalize_branch_name(text: str) -> str:
    branch = str(text or "").splitlines()[0].strip()
    branch = branch.strip("`").strip()
    if branch.lower().startswith("refs/heads/"):
        branch = branch[11:].strip()
    return branch


