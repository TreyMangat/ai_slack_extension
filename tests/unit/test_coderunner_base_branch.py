def test_repo_has_changes_detects_dirty_worktree(tmp_path: Path) -> None:
    repo = tmp_path / "repo-dirty"
    repo.mkdir(parents=True, exist_ok=True)
    coderunner_mod._run_command(["git", "init"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "config", "user.name", "test"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "config", "user.email", "test@example.com"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    (repo / "hello.txt").write_text("hello\n", encoding="utf-8")
    coderunner_mod._run_command(["git", "add", "-A"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "commit", "-m", "init"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    baseline = coderunner_mod._current_head_sha(repo_path=repo)  # noqa: SLF001

    (repo / "hello.txt").write_text("hello world\n", encoding="utf-8")
    has_changes, status, current_head = coderunner_mod._repo_has_changes(  # noqa: SLF001
        repo_path=repo,
        baseline_head=baseline,
    )
    assert has_changes is True
    assert status
    assert current_head == baseline


def test_repo_has_changes_detects_clean_precommitted_changes(tmp_path: Path) -> None:
    repo = tmp_path / "repo-precommit"
    repo.mkdir(parents=True, exist_ok=True)
    coderunner_mod._run_command(["git", "init"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "config", "user.name", "test"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "config", "user.email", "test@example.com"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    (repo / "hello.txt").write_text("hello\n", encoding="utf-8")
    coderunner_mod._run_command(["git", "add", "-A"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "commit", "-m", "init"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    baseline = coderunner_mod._current_head_sha(repo_path=repo)  # noqa: SLF001

    (repo / "hello.txt").write_text("hello world\n", encoding="utf-8")
    coderunner_mod._run_command(["git", "add", "-A"], cwd=repo, timeout_seconds=30)  # noqa: SLF001
    coderunner_mod._run_command(["git", "commit", "-m", "update"], cwd=repo, timeout_seconds=30)  # noqa: SLF001

    has_changes, status, current_head = coderunner_mod._repo_has_changes(  # noqa: SLF001
        repo_path=repo,
        baseline_head=baseline,
    )
    assert has_changes is True
    assert status == ""
    assert current_head and current_head != baseline
