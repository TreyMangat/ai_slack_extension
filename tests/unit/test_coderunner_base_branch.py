def test_build_no_change_retry_prompt_mentions_attempt_context(tmp_path: Path) -> None:
    repo_path = tmp_path / "repo"
    repo_path.mkdir(parents=True, exist_ok=True)
    (repo_path / "app.py").write_text("print('hello')\n", encoding="utf-8")
    (repo_path / "README.md").write_text("# Demo\n", encoding="utf-8")

    prompt = coderunner_mod._build_no_change_retry_prompt(  # noqa: SLF001
        original_prompt="Base prompt",
        spec={
            "title": "Update greeting",
            "problem": "Change hello text",
            "acceptance_criteria": ["Greeting text is updated"],
        },
        repo_path=repo_path,
        attempt_number=2,
        total_attempts=2,
        previous_reply="No changes needed",
    )
    assert "produced no repository file changes" in prompt
    assert "attempt 1/2" in prompt
    assert "Greeting text is updated" in prompt


